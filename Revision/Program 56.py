#Program to convert Celsius to Farenheit
celsius = float(input("Enter temperature in Celsius: "))
farenheit = (celsius * 9/5) + 32
print(f"{celsius} degree Celsius is equal to {farenheit} degree Farenheit.")
#Program to convert Farenheit to Celsius
farenheit = float(input("Enter temperature in Farenheit: "))
celsius = (farenheit - 32) * 5/9
print(f"{farenheit} degree Farenheit is equal to {celsius} degree Celsius.")

      